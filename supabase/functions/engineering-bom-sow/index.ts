// ─── BOM unit sanitizer ───────────────────────────────────────────────────────
// Groq sometimes puts descriptive text in the `unit` field.
// Clamp any value longer than 12 characters or containing spaces back to "unit".
const VALID_UNITS = new Set([
  'unit','set','pc','pcs','lot','m','m²','m³','kg','L','roll','pair',
  'panel','breaker','tank','battery','length','run','assembly','bag',
  'sheet','drum','box','can','tube','bar','bundle',
]);
function sanitizeBomItems(items: unknown[]): unknown[] {
  return items.map((item: unknown) => {
    const it = item as Record<string, unknown>;
    const raw = String(it.unit || 'unit').trim();
    const clean = VALID_UNITS.has(raw.toLowerCase()) ? raw : (raw.length > 12 || raw.includes(' ') ? 'unit' : raw);
    return { ...it, unit: clean };
  });
}

// ─── Groq LLM helper — 6-model fallback chain ────────────────────────────────
//
// Order: best prose quality first, highest capacity last.
// On 429 (rate limit): silently try next model.
// On any other error: fail fast — don't waste remaining models on a code bug.
//
const GROQ_FALLBACK_CHAIN = [
  "llama-3.3-70b-versatile",                    // 1st: best quality,   100K TPD
  "openai/gpt-oss-120b",                         // 2nd: largest model,  200K TPD, strict JSON schema
  "meta-llama/llama-4-scout-17b-16e-instruct",   // 3rd: 500K TPD, 30K TPM, fast
  "openai/gpt-oss-20b",                          // 4th: 200K TPD, strict JSON schema
  "qwen/qwen3-32b",                              // 5th: 500K TPD, 60 RPM
  "llama-3.1-8b-instant",                        // 6th: last resort, 500K TPD
];

async function callGroq(prompt: string): Promise<string> {
  const GROQ_KEY = Deno.env.get("GROQ_API_KEY") || "";
  if (!GROQ_KEY) throw new Error("GROQ_API_KEY secret is not set.");

  let lastError = "";

  for (const model of GROQ_FALLBACK_CHAIN) {
    const res = await fetch("https://api.groq.com/openai/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        "Authorization": `Bearer ${GROQ_KEY}`,
      },
      body: JSON.stringify({
        model,
        messages: [{ role: "user", content: prompt }],
        temperature: 0.2,
        response_format: { type: "json_object" },
      }),
    });

    // Rate limit — try next model silently
    if (res.status === 429) {
      const errText = await res.text();
      console.warn(`Model ${model} rate-limited (429), trying next. Detail: ${errText.slice(0, 120)}`);
      lastError = `${model}: rate limit`;
      continue;
    }

    // Real error (bad model ID, auth failure, server error) — fail fast, don't waste chain
    if (!res.ok) {
      const err = await res.text();
      throw new Error(`Groq API error ${res.status} on model ${model}: ${err}`);
    }

    const data = await res.json();
    const content = data?.choices?.[0]?.message?.content || "{}";
    console.log(`BOM/SOW generated by: ${model}`);
    return content;
  }

  // All 6 models exhausted
  throw new Error(`All AI models are at capacity (tried: ${GROQ_FALLBACK_CHAIN.join(", ")}). Last: ${lastError}. Try again in 1 hour.`);
}

